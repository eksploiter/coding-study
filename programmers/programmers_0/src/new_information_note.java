//'.length'
// - 사용처: 배열
// - 설명: 배열의 길이를 반환하는 속성(property) 입니다.
// - 예시:
// int[] num_list = {1, 2, 3, 4, 5};
// int length = num_list.length; //배열의 길이 반환
// - 특징: 메서드가 아니라 속성이기 때문에 괄호 '()'가 붙지 않습니다.

//'.length()'
// - 사용처: 문자열 (String)
// - 설명: 문자열의 길이를 반환하는 메서드(method) 입니다.
// - 예시:
// String number = 12345;
// int length = number.length(); // 문자열의 실이 반환
// - 특징: 메서드이기 때문에 호출 시 괄호 '()'가 붙습니다.

