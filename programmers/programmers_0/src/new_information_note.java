// 1. 데이터 타입:
// - '.length' 는 배열(array)에서 사용됩니다.
// - '.length()'는 문자열(String)에서 사용됩니다.

// 2. 구현 방식:
// - '.length' 는 속성(property)으로, 객체의 상태를 나타내며, 직접 접근하여 값을 가져옵니다.
// - '.length()' 는 메서드(method)로, 호출 시 특정 연산이나 처리를 통해 값을 반환합니다.

